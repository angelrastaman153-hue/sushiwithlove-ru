// Нормализатор имени для поиска (lower + схлопывание пробелов + убрать знаки)
function norm($s) {
    $s = mb_strtolower($s, 'UTF-8');
    $s = preg_replace('/[«»"\'\.,:;!?()\[\]{}#№]/u', ' ', $s);
    $s = preg_replace('/\s+/u', ' ', $s);
    return trim($s);
}

