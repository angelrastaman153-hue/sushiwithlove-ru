$stmt = $pdo->prepare("UPDATE menu_items SET description=? WHERE LOWER(TRIM(name))=LOWER(TRIM(?))");
$count = 0;
$notFound = [];

foreach ($fixes as $f) {
    $stmt->execute([$f['desc'], $f['name']]);
    if ($stmt->rowCount() > 0) {
        $count++;
        echo "OK: {$f['name']}\n";
    } else {
        $notFound[] = $f['name'];
    }
}

echo "\nОбновлено: $count позиций\n";
