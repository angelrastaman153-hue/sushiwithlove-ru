$apply = isset($_GET['apply']) && $_GET['apply'] === '1';

$pdo = db();
$rows = $pdo->query('SELECT id, name, flavor_description FROM menu_items WHERE id IN ('.implode(',', array_keys($FLAVORS)).')')->fetchAll(PDO::FETCH_ASSOC);
$byId = array();
foreach ($rows as $r) $byId[(int)$r['id']] = $r;

echo '<html><head><meta charset="utf-8"><title>Вкусные описания</title>';
echo '<style>body{font-family:system-ui;max-width:1100px;margin:20px auto;padding:0 20px}table{width:100%;border-collapse:collapse}td,th{border:1px solid #ddd;padding:8px;font-size:14px;vertical-align:top}.cur{color:#666;background:#fafafa}.new{color:#090;background:#efe}</style>';
echo '</head><body>';
echo '<h1>Вкусные описания для '.count($FLAVORS).' роллов</h1>';

if ($apply) {
    $upd = $pdo->prepare('UPDATE menu_items SET flavor_description = :f WHERE id = :id');
    $n = 0;
    foreach ($FLAVORS as $id => $text) {
        $upd->execute(array(':f' => $text, ':id' => $id));
        $n++;
    }
    echo '<p style="color:#090;font-weight:700">✅ Применено: обновлено '.$n.' позиций.</p>';
    echo '<p><a href="/api/admin/">← Назад в админку</a></p>';
    echo '<p style="color:#c00"><b>Важно:</b> удали файл <code>/api/menu/apply_flavors.php</code> с сервера.</p>';
} else {
    echo '<p><a href="?apply=1" style="background:#c00;color:#fff;padding:10px 16px;border-radius:6px;text-decoration:none;font-weight:700">Применить</a> &nbsp; <a href="/api/admin/">отменить</a></p>';
}

echo '<table><thead><tr><th>ID</th><th>Название</th><th>Текущее вкусное</th><th>Новое вкусное</th></tr></thead><tbody>';
foreach ($FLAVORS as $id => $text) {
    $r = isset($byId[$id]) ? $byId[$id] : array('name' => '(нет в БД)', 'flavor_description' => '');
    echo '<tr>';
    echo '<td>'.$id.'</td>';
    echo '<td>'.htmlspecialchars($r['name'], ENT_QUOTES, 'UTF-8').'</td>';
    echo '<td class="cur">'.htmlspecialchars((string)$r['flavor_description'], ENT_QUOTES, 'UTF-8').'</td>';
    echo '<td class="new">'.htmlspecialchars($text, ENT_QUOTES, 'UTF-8').'</td>';
    echo '</tr>';
}
echo '</tbody></table>';
echo '</body></html>';
